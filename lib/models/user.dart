class User {
  final int id;
  final String name;
  final String lastName;
  final String email;
  final String phone;
  final String birthDate;
  final String token;

  User({
    required this.id,
    required this.name,
    required this.lastName,
    required this.email,
    required this.phone,
    required this.birthDate,
    required this.token,
  });
}
